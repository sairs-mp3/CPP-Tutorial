#include <iostream>

using namespace std;

int main(){

    // Greater than test 
   if (5 > 3){

       cout << "Hello, World\n";

   }

    // Less than test
    if (5 < 10){

        cout << "Hello, World!\n";

    }

    // Greater than or equal to test
    if (5 >= 3){

       cout << "Hello, World\n";

   }

   // Less than or equal to test
    if (2 <= 3){

       cout << "Hello, World\n";

   }

    // Equality test
    if(3 == 3){

        cout << "Hello, World!\n";

    }

    // Not equal test
    if (4 != 5){

        cout << "Hello, World!\n";

    }

    return 0;
}